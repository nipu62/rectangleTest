class Rectangle:
    """
    A class representing a rectangle.
    Attributes:
    - length (float): The length of the rectangle.
    - width (float): The width of the rectangle.
    """

    def __init__(self, length=1, width=1):
        """
        Initializes a Rectangle object.
        - Ensure length and width are positive numbers.
        - Raise TypeError if input is not numeric.
        - Raise ValueError if length or width is negative.
        """

    def area(self):
        """
        Returns the area of the rectangle.
        """

    def perimeter(self):
        """
        Returns the perimeter of the rectangle.
        """

    def __str__(self):
        """
        Returns a string representation of the rectangle as:
        'Rectangle: Length=x, Width=y'
        """
